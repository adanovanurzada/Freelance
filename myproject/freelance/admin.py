from django.contrib import admin
from modeltranslation.admin import TranslationAdmin, TranslationInlineModelAdmin
from .models import *

class OfferInline(admin.TabularInline, TranslationInlineModelAdmin):
    model = Offer
    extra = 1

class ReviewInline(admin.TabularInline, TranslationInlineModelAdmin):
    model = Review
    extra = 1


@admin.register(UserProfile, Skill, SocialLinks, Category)
class SimpleTranslationAdmin(TranslationAdmin):
    class Media:
        js = (
            'https://ajax.googleapis.com/ajax/libs/jquery/1.9.1/jquery.min.js',
            'https://ajax.googleapis.com/ajax/libs/jqueryui/1.10.2/jquery-ui.min.js',
            'modeltranslation/js/tabbed_translation_fields.js',
        )
        css = {
            'screen': ('modeltranslation/css/tabbed_translation_fields.css',)
        }


